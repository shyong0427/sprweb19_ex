package pack.model;

public class JikwonDto {
	private String jikwon_no, jikwon_name, buser_tel, jikwon_jik, gogek_su;

	public String getJikwon_no() {
		return jikwon_no;
	}

	public void setJikwon_no(String jikwon_no) {
		this.jikwon_no = jikwon_no;
	}

	public String getJikwon_name() {
		return jikwon_name;
	}

	public void setJikwon_name(String jikwon_name) {
		this.jikwon_name = jikwon_name;
	}

	public String getBuser_tel() {
		return buser_tel;
	}

	public void setBuser_tel(String buser_tel) {
		this.buser_tel = buser_tel;
	}

	public String getJikwon_jik() {
		return jikwon_jik;
	}

	public void setJikwon_jik(String jikwon_jik) {
		this.jikwon_jik = jikwon_jik;
	}

	public String getGogek_su() {
		return gogek_su;
	}

	public void setGogek_su(String gogek_su) {
		this.gogek_su = gogek_su;
	}
}